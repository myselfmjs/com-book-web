public class FsClass {

    private String str01;

    private String str02;


    public FsClass() {
    }

    public FsClass(String str01, String str02) {
        this.str01 = str01;
        this.str02 = str02;
    }

    public String getStr01() {
        return str01;
    }

    public void setStr01(String str01) {
        this.str01 = str01;
    }

    public String getStr02() {
        return str02;
    }

    public void setStr02(String str02) {
        this.str02 = str02;
    }
}
